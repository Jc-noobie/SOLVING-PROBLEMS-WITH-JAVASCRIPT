const max = 9 ;


function printBoard(board) {

    let output = '' ;

    for(let i = 0 ; i < max ; i++) {

        if(i % 3 === 0 ) {
            output += ' -------------------------\n' ;
        }

        output += " | " ;

        for(let j = 0 ; j < max; j++) {
            if((j + 1) % 3 === 0 ) {
                output += board[i][j] + " | "
            } else {
                output += board[i][j] + " " 
            }
        }

        output += '\n'
    }

    output += " -------------------------\n"
    return output ; 
}



function isValid(board) {

    for(let i = 0 ;  i < max ; i++) {
        for(let j = 0 ; j < max ; j++) {
            for(let k = j + 1 ; k < max; k++) {
                if(board[i][j] !== "0" && board[i][j] === board[i][k]) {
                    return false ;
                }
            }
        }
    }

    return true ;
}

